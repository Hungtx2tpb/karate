package auto.utils.tranning.lesson2;

public class IfElseDemo {
    public static void main(String[] args) {
        int number = 10;
        if (number > 0) {
            System.out.println("Số là số dương.");
        } else if (number < 0) {
            System.out.println("Số là số âm.");
        } else {
            System.out.println("Số bằng.");
        }
    }
}
